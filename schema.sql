-- ==============================================================
-- Database Schema for Champuchino Cafe Reservation System
-- Designed for Supabase (PostgreSQL)
-- ==============================================================

-- เปิดใช้งาน UUID extension (ถ้ายังไม่เปิด)
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- 1. Table: users (ข้อมูลลูกค้าและแอดมิน)
CREATE TABLE users (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    line_id TEXT UNIQUE NOT NULL,
    full_name TEXT NOT NULL,
    role TEXT DEFAULT 'student', -- 'student', 'lecturer', 'staff', 'admin'
    phone TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Table: cafe_tables (ข้อมูลโต๊ะ)
CREATE TABLE cafe_tables (
    id INT8 PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    table_number INT2 NOT NULL,
    capacity INT2 DEFAULT 8,
    status TEXT DEFAULT 'available' -- 'available', 'maintenance'
);

-- 3. Table: bookings (ข้อมูลการจอง)
CREATE TABLE bookings (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    user_id UUID REFERENCES users(id) ON DELETE CASCADE,
    table_id INT8 REFERENCES cafe_tables(id) ON DELETE CASCADE,
    booking_date DATE NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    guest_count INT2 NOT NULL CHECK (guest_count > 0 AND guest_count <= 8),
    status TEXT DEFAULT 'pending', -- 'pending', 'approved', 'cancelled'
    created_at TIMESTAMPTZ DEFAULT NOW()
);

