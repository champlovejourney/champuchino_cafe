-- ==============================================================
-- RLS Setup for Pre-order
-- ==============================================================
ALTER TABLE menu_categories ENABLE ROW LEVEL SECURITY;
ALTER TABLE menu_items ENABLE ROW LEVEL SECURITY;
ALTER TABLE booking_orders ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public can view menu categories" ON menu_categories FOR SELECT USING (true);
CREATE POLICY "Public can view menu items" ON menu_items FOR SELECT USING (true);

-- ให้ User สามารถจัดการรายการอาหารในคิวจองของตัวเองได้
CREATE POLICY "Users can manage their own booking orders" ON booking_orders 
  FOR ALL USING (
    EXISTS (
      SELECT 1 FROM bookings 
      WHERE bookings.id = booking_orders.booking_id 
      AND bookings.user_id = auth.uid()
    )
  );
