-- ==============================================================
-- Row Level Security (RLS) Setup
-- ==============================================================

-- เปิดใช้งาน RLS สำหรับความปลอดภัย
ALTER TABLE users ENABLE ROW LEVEL SECURITY;
ALTER TABLE cafe_tables ENABLE ROW LEVEL SECURITY;
ALTER TABLE bookings ENABLE ROW LEVEL SECURITY;

-- 1. Policies ของ users
-- ให้ user แต่ละคนมองเห็นและแก้ไขเฉพาะข้อมูลของตัวเองได้ (ใช้ auth.uid() ของ Supabase)
CREATE POLICY "Users can view own data" ON users
    FOR SELECT USING (auth.uid() = id);

CREATE POLICY "Users can update own data" ON users
    FOR UPDATE USING (auth.uid() = id);

-- 2. Policies ของ cafe_tables
-- ให้ทุกคนที่เข้าเว็บ (public) สามารถดูข้อมูลโต๊ะว่างได้
CREATE POLICY "Public can view tables" ON cafe_tables
    FOR SELECT USING (true);

-- 3. Policies ของ bookings
-- ให้ทุกคนสามารถดูคิวการจองได้ (เพื่อเช็คเวลาที่ว่าง)
CREATE POLICY "Public can view bookings" ON bookings
    FOR SELECT USING (true);

-- ให้ user สามารถเพิ่มคิวจองได้ แต่ต้องเป็น id ของตัวเองเท่านั้น
CREATE POLICY "Users can insert own bookings" ON bookings
    FOR INSERT WITH CHECK (auth.uid() = user_id);

-- ให้ user แก้ไข (ยกเลิก) คิวของตัวเองได้
CREATE POLICY "Users can update own bookings" ON bookings
    FOR UPDATE USING (auth.uid() = user_id);

-- ==============================================================
-- Food & Beverage Pre-order Schema (ระบบสั่งอาหารล่วงหน้า)
-- ==============================================================

-- 4. Table: menu_categories (หมวดหมู่เมนู)
CREATE TABLE menu_categories (
    id INT8 PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name TEXT NOT NULL
);

-- 5. Table: menu_items (รายการเมนูอาหาร/เครื่องดื่ม)
CREATE TABLE menu_items (
    id INT8 PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    category_id INT8 REFERENCES menu_categories(id) ON DELETE SET NULL,
    name TEXT NOT NULL,
    description TEXT,
    price NUMERIC(10, 2) NOT NULL,
    image_url TEXT,
    is_available BOOLEAN DEFAULT TRUE
);

-- 6. Table: booking_orders (รายการอาหารที่สั่งพร้อมการจอง)
CREATE TABLE booking_orders (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    booking_id UUID REFERENCES bookings(id) ON DELETE CASCADE,
    menu_item_id INT8 REFERENCES menu_items(id) ON DELETE CASCADE,
    quantity INT2 NOT NULL CHECK (quantity > 0),
    price_at_time NUMERIC(10, 2) NOT NULL -- เก็บราคา ณ วันที่สั่ง ป้องกันราคาเปลี่ยนในอนาคต
);

